    protected void datatable() {
        Object[] Baris = {"No.Id", "Nama", "Alamat", "Tlp", "Email"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelcustomer.setModel(tabmode);
        String sql = "select * from customer";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String noid = hasil.getString("id");
                String tnama = hasil.getString("nama");
                String talamat = hasil.getString("alamat");
                String ttlp = hasil.getString("tlp");
                String temail = hasil.getString("email");

                String[] data = {noid, tnama, talamat, ttlp, temail};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

